    public void generarComprobante(){
        try {
            Container contenedor= this.vistaReporte;
            contenedor.setLayout(new BorderLayout());
        
            //Agregando par√°metros
            
            Map<String,Object> param= new HashMap<>();
            param.put("idcomanda", idComanda);
            param.put("tipo", tipo);
            param.put("codigo", codigo);
            param.put("cliente", cliente);
            param.put("documento", documento);
            param.put("total", total);
            param.put("gravada", TG);
            param.put("igv", igv);
            param.put("totalLetras", totalLetras);
            
            JRViewer vistaRPT= new clsReporte().reporteInterno("rptComprobante.jasper", param);
            contenedor.add(vistaRPT);
            this.vistaReporte.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    public void generarTotalALetras(){
        BigDecimal number = new BigDecimal(Float.parseFloat(total));
        long parteEntera = number.longValue();
        BigDecimal fraccion = number.remainder(BigDecimal.ONE);
        Numero_a_Letra d= new Numero_a_Letra();
        DecimalFormatSymbols separadoresPersonalizados = new DecimalFormatSymbols();
        separadoresPersonalizados.setDecimalSeparator('.');
        DecimalFormat formato1 = new DecimalFormat("#.00", separadoresPersonalizados);
        String letras= d.Convertir(String.valueOf(parteEntera), true);
        String decimlaSinCero=formato1.format(fraccion);
        String decimalsinceroypunt=substring(decimlaSinCero,1,3);
        totalLetras= " "+letras.toUpperCase()+" "+"CON"+" "+decimalsinceroypunt+"/100 SOLES";
        
    }
